package com.magus.bd.vo;

public class ProgramTotal {

	private String year;
	private float changeUnit;
	private float closeUnit;
	private float newUnit;
	private float amountBefore;
	private float amountAfter;
	private float amountLess;

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public float getChangeUnit() {
		return changeUnit;
	}

	public void setChangeUnit(float changeUnit) {
		this.changeUnit = changeUnit;
	}

	public float getCloseUnit() {
		return closeUnit;
	}

	public void setCloseUnit(float closeUnit) {
		this.closeUnit = closeUnit;
	}

	public float getNewUnit() {
		return newUnit;
	}

	public void setNewUnit(float newUnit) {
		this.newUnit = newUnit;
	}

	public float getAmountBefore() {
		return amountBefore;
	}

	public void setAmountBefore(float amountBefore) {
		this.amountBefore = amountBefore;
	}

	public float getAmountAfter() {
		return amountAfter;
	}

	public void setAmountAfter(float amountAfter) {
		this.amountAfter = amountAfter;
	}

	public float getAmountLess() {
		return amountLess;
	}

	public void setAmountLess(float amountLess) {
		this.amountLess = amountLess;
	}

}
