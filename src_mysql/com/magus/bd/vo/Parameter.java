package com.magus.bd.vo;

public class Parameter {

	private float begin;
	private float end;

	public float getBegin() {
		return begin;
	}

	public void setBegin(float begin) {
		this.begin = begin;
	}

	public float getEnd() {
		return end;
	}

	public void setEnd(float end) {
		this.end = end;
	}

}
